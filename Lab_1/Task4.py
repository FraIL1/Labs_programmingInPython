numbers = [1, 5, 3, 654, 212, 111, 2, 98, 54, 100]

# вывод всех чисел
print(numbers)

# вывод максимального и минимального числа
max_num = max(numbers)
min_num = min(numbers)
print(max_num, min_num, sep=',')

# вывод суммы всех элементов списка
print(sum(numbers))

# вывод сортировки по возрастанию
print(sorted(numbers))